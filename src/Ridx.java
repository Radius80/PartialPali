public class Ridx {
private Comparable value;
private int idx;

/**
 * Constructor for indx having class
 */
public Ridx() {

}
/**
 * Set Value
 * @param v value
 */
public void setV(Comparable v) {
    value = v;
}

/**
 * Get value
 * @return value
 */
public Comparable getV() {
    Comparable dis = value;
    return dis;
}

/**
 * Set indx
 * @param v value
 */
public void setI(int i) {
    idx = i;    
}

/**
 * Get idx
 * @return value
 */
public int getI() {
    int dis = idx;
    return dis;
}

}
